#include <stdio.h>

int		match(char *s1, char *s2)
{
	if (*s2 == '*')
	{
		if (!(*(s2 + 1)))
			return (1);
		else if (*(s2 + 1) != *s1)
			match(++s1, s2);
		else
			s2++;
	}
	if (*s1 == '\0' && *s2 == '\0')
	{
		return (1);
	}
	if (*s1 && *s1 == *s2)
		return (match(++s1, ++s2));
	return (0);
}
