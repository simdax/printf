int	ft_isalnum(int c)
{
	unsigned char	cc;

	cc = (unsigned char)c;
	if (('A' <= cc && cc <= 'Z') ||
		('a' <= cc && cc <= 'z') ||
		('0' <= cc && cc <= '9'))
		return (1);
	return (0);
}
