int	ft_isalnum(int c)
{
	unsigned char	cc;

	cc = (unsigned char)c;
	if ('A' <= cc <= 'Z' || 'a' <= cc <= 'Z' || '0' <= cc <= '9')
		return (1);
	return (0);
}
