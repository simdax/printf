#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	value;

	while (n > 10)
	{
		value = ('0' + n % 10);
		write(fd, &value, 1);
		ft_putnbr_fd(n / 10, fd);
	}
}
