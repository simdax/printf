#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	l;

	l = 0;
	while (src[l] != '\0')
	{
		dst[l] = src[l];
		l++;
	}
	dst[l] = '\0';
	return (dst);
}
