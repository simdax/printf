int	ft_sqrt(int nb)
{
	if (nb == 4)
		return (2);
	else if (nb < 4 || nb > 30)
		return (0);
	else
		if (nb % 2 == 0)
			ft_sqrt(nb / 2);
		else
			return (0);
}
