int		ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	i = 0;
	while (i < 50000)
	{
		if (nb < i * i)
			return (0);
		else if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}

