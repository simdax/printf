char	*ft_concat_params(int len, char **argv)
{
	int		i;
	char	*res;

	while (argv[len])
	{
		i = 0;
		while (*(argv[len]))
		{
			*res = *(argv[len]);
			res++;
			i++;
		}
		len++;
	}
	*res = '\0';
}
